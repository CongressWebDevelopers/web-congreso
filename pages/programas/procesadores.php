        <h1>Procesadores de Lenguajes</h1>
        <p>El objetivo principal es enseñar al congresista los problemas y técnicas que se
            plantean en la construcción de procesadores, traductores, compiladores e intérpretes de los
            distintos lenguajes de programación. El estudio de estas técnicas permite una visión
            más amplia de los lenguajes de programación, habitualmente estudiados desde el punto de
            vista del programador y no del constructor de compiladores o intérpretes..</p>
        <p>
            Otro aspecto importante es el diseño de lenguajes de programación, y de sus compiladores o
            intérpretes, que es de interés para aquellas aplicaciones que necesitan la definición de un
            lenguaje para su manejo. Otras aplicaciones informáticas que utilizan las técnicas de los
            Procesadores de Lenguaje son los conversores de formatos, editores sensibles al contexto,
            generadores de código, verificadores estáticos de programas, construcción de herramientas
            CASE, etc.</p>

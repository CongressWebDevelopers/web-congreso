	<h1>Interacción Háptica</h1>
	<p>Los dispositivos ‘multi-touch’, como el iPad, se han vuelto en los últimos dos años cada vez más populares. Hoy en día no sólo se utilizan para navegar y enviar correos electrónicos, sino también como un medio para nuevos campos de aplicación. Algo particular de estos dispositivos (tablets), casi una necesidad, es la falta de retroalimentación ‘háptica’ (percepción háptica), lo que hace que sea difícil establecer parámetros de uso con precisión. En otras palabras, una respuesta al tacto que aumente la experiencia de la interacción.</p>
	<p>Modulares Interface B.A., un proyecto del diseñador alemán Florian Born, es una interfaz física para iPad que se compone de perillas, botones y controles deslizantes para proporcionar precisión y retroalimentación háptica para operar la interfaz digital.</p>
	<p>Se trata de una variedad de controladores físicos que expanden el uso del dispositivo. Con el uso de imanes, los diferentes controladores se pueden organizar fácilmente en el iPad. Aparece así una interfaz modular física en contacto con la pantalla digital. El sistema consta de tres partes diferentes: los controladores físicos (botones, deslizantes y perillas), fabricados en aluminio conductor para transmitir la descarga eléctrica de la piel; un marco, hecho de aluminio y plástico, en el que se inserta el iPad y el  Software, que se ejecuta como una aplicación en el iPad.</p>

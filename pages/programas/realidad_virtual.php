        <h1>Realidad Virtual</h1>
        <p>La realidad virtual es un entorno de escenas u objetos de apariencia real, generado mediante tecnología informática, que crea en el usuario la sensación de estar inmerso en él. Dicho entorno es contemplado por el usuario a través normalmente de un dispositivo conocido como gafas o casco de realidad virtual. Este puede ir acompañado de otros dispositivos, como guantes o trajes especiales, que permiten una mayor interacción con el entorno así como la percepción de diferentes estímulos que intensifican la sensación de realidad.</p>
        <p>La aplicación de la realidad virtual, aunque centrada inicialmente en el terreno del entretenimiento y de los videojuegos, se ha extendido a otros muchos campos, como la medicina, la arqueología, la creación artística, el entrenamiento militar o las simulaciones de vuelo.</p>
        <p>La virtualidad establece una nueva forma de relación entre el uso de las coordenadas de espacio y de tiempo, supera las barreras espaciotemporales y configura un entorno en el que la información y la comunicación se nos muestran accesibles desde perspectivas hasta ahora desconocidas al menos en cuanto a su volumen y posibilidades. La realidad virtual permite la generación de entornos de interacción que separen la necesidad de compartir el espacio-tiempo, facilitando en este caso nuevos contextos de intercambio y comunicación.</p>

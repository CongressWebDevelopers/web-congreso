	<h1>Bases de Datos Multidimensionales</h1>
	<p>Las bases de datos multidimensionales se utilizan principalmente para crear aplicaciones OLAP y pueden verse como bases de datos de una sola tabla, su peculiaridad es que por cada dimensión tienen un campo (o columna), y otro campo por cada métrica o hecho, es decir estas tablas almacenan registros cuyos campos son de la forma:
{(d_1, d_2, d_3, ..., f_1, f_2, f_3, ...)}

Donde los campos '{d_i}' hacen referencia a las dimensiones de la tabla, y los campos '{f_i}' a las métricas o hechos que se quiere almacenar, estudiar o analizar.</p>

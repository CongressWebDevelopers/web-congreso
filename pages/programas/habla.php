	<h1>Procesamiento de Habla</h1>
	<p>Los sistemas interactivos de procesamiento del habla son sistemas informáticos que reciben como entrada frases del lenguaje natural expresadas oralmente y generan como salida frases en lenguaje natural expresadas asimismo de forma oral. Estos sistemas se suelen usar por diversas empresas para proporcionar información a sus clientes de forma automática (por ejemplo, horarios de salida de aviones, partes meteorológicos, estado de cuentas bancarias, etc.). Prueba del gran interés actual de estos sistemas es un estudio de The Kelsey Group (Estados Unidos), según el cual, el gasto mundial en servicios basados en procesamiento del habla estará en torno a 41.000 millones de dólares en el año 2005, con un incremento medio de 60-65% a partir de ese año. Este gran interés está motivado por diversas razones. Por una parte, el acceso a servicios utilizando pequeñas interfaces basadas en texto (p. e. teléfonos móviles WAP, PDA's, etc.) está limitado por la dificultad de interactuar con una pequeña pantalla, difícil de leer y poco apropiada para obtener determinados tipos de información. Por otra parte, la utilización de dichos dispositivos no es posible para usuarios con determinadas discapacidades (p. e. invidentes), o bien, no es aconsejable en determinadas situaciones en que es necesario tener las manos y los ojos ocupados en otra tarea (p. e. conduciendo un automóvil). La finalidad de los sistemas interactivos de procesamiento del habla es solventar estas limitaciones, permitiendo el acceso a servicios automáticos de información usando el habla como canal de interacción.</p>

	<h1>Wearables</h1>
	<p>Wearable hace referencia al conjunto de aparatos y dispositivos electrónicos que se incorporan en alguna parte de nuestro cuerpo interactuando continuamente con el usuario y con otros dispositivos con la finalidad de realizar alguna función específica, relojes inteligentes o smartwatchs, zapatillas de deportes con GPS incorporado y pulseras que monitorizan nuestro estado de salud son ejemplos entre otros de este tipo tecnología que se encuentra cada vez más presente en nuestras vidas.</p>
	<p>Tal y como hemos expuesto anteriormente la tecnología wearable hace referencia a todos los productos que incorporan un microprocesador y que utilizamos diariamente formando parte de nosotros, dentro de esta definición no consideramos wearable a nuestra televisión del salón, a la cafetera de la cocina o al ebook que utilizamos para leer nuestras libros, puesto que aunque sean dispositivos electrónicos que poseen microprocesadores y los usamos diariamente no forman parte de nosotros dado a que no son "llevables" o "vestibles" en cambio lentes, gafas, pulseras, relojes o prendas de vestir son productos llevables los cuales si le añadimos uno o varios microprocesadores electrónicos obtenemos productos wearables.</p>

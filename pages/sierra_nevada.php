            <h1>Visita a Sierra Nevada </h1>
            <div id="img_sierra">
                <img alt="Imagen Sierra Nevada" src="../images/sierra.jpg" />
            </div>

            <p>Enclavada en la parte central de la Cordillera Penibética, el macizo de Sierra Nevada constituye la más extensa de las áreas montañosas ibéricas dotadas de unidad estructural propia. Denominada por los árabes de la Edad Media como “Sierra del Sol”, alberga no sólo una de las floras mas valiosas de todo el continente europeo, sino que además esconde un rico patrimonio cultural e histórico acumulado durante siglos.</p>
            <p>La estación de esquí se encuentra en pleno corazón del Parque Nacional de Sierra Nevada, siendo ésta la más alta de España y la más merdional de Europa. Estas condiciones hacen que la estación de esquí de Sierra Nevada , sea la que tenga más horas de Sol de toda Europa; convirtiéndola también en la más visitada de España. Es más, Sierra Nevada está considerada la estación de esquí con más ambiente nocturno de Europa.</p>
            <p>Sierra Nevada ofrece una multitud de actividades, y rutas de alta y baja montaña, en todas las estaciones del año. De hecho, en buena temporada, incluso en verano se pueden encontrar pistas de esquí abiertas.</p>

            <div id="img_esqui" class="fl_right">
                <img alt="Imagen esquiando" src="../images/esqui.jpg" />
            </div>


            <h3>Itinerario</h3>
            <p>La visita a Sierra Nevada será gratuita para todos los inscritos en el congreso, en un día aún por definir. Dicha visita durará todo el día. Por la mañana, los inscritos dispondrán de una hora de clases en grupo, según su nivel; pueden elegir tanto la modalidad de esquí, como snowboard. Tras esta hora de clase, se dispondrá de 2 horas de libre disposición para que los inscritos esquíen por las distintas pistas, según su nivel. Por la tarde, se realizará una caminata de unas 4 horas, hasta el Veleta. Por supuesto, tanto, el transporte, como el equipamiento y el forfait son gratuitos.</p>
            <p>Resumiendo, el itirerario será el siguiente:</p>
            <ol>
                <li>Traslado desde Granada a Sierra Nevada en autobuses.</li>
                <li>Entrega del equipamiento y forfait para poder entrar en las pistas. Se debe elegir una modalidad: esquí o snowboard</li>
                <li>Clase en grupo de una hora, según el nivel y la modalidad</li>
                <li>Dos horas de esquí libre.</li>
                <li>Comida en grupo</li>
                <li>Caminata guiada hasta el Veleta</li>
                <li>Traslado de vuelta a Granada</li>
            </ol>

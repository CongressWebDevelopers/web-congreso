    <h1>La ETSIIT</h1>
    
    <div id="img_etsiit" class="fl_right" >
      <img alt="Imagen etsiit" src="../images/etsiit-1.jpg"/>
    </div>


    <p id="ejemplo">La ETSIIT (Escuela Técnica Superior de Ingeniería Informática), es un centro docente perteneciente a la Universidad pública de Granada (UGR) donde los estudiantes de Granada, o de otras ciudades o lugares, cursan los estudios de Grado en Ingeniería Informática y Grado en Ingeniería de Telecomunicaciones; en el marco del Espacio Europeo de Educación Superior (EEES) </p>
      
    <p>En nuestra facultad se ofertan todas las especializaciones del Grado en Ingeniería Informática consensuadas en el marco docente, y tres de las cuatro especializaciones del Grado en Ingeniería de Telecomunicaciones: "Sistemas de Telecomunicación", "Telemática" y "Sistemas Electrónicos" </p>
      
    <p>La ETSIIT está dividida en dos edificios comunicados entre sí. Cada edificio consta de 5 plantas más un semisótano. Concretamente:
    </p>
      <ul>
        <li>En el primer edificio se encuentran la cafetería y el comedor universitario, los servicios administrativos, la biblioteca y los distintos departamentos.</li>
        <li>En el segundo edificio se encuentran las aulas de teoría y prácticas.</li>
      </ul>                  
      

        <h1>Prince2</h1>
        <p>PRINCE2® (PRojects IN Controlled Environment) es un método estructurado de gestión de proyectos. Es una aproximación a las “buenas 		prácticas” para la gestión de todo tipo de proyectos que se ha convertido en el estándar de facto para la organización, gestión y 		control de proyectos.</p>
        <p> El método divide los proyectos en fases manejables permitiendo el control eficiente de los recursos y el control periódico de su 		evolución. PRINCE2 está "basado en los productos", es decir, los planes del proyecto se centran en obtener resultados concretos, y no 		sólo en la planificación de las actividades que se llevan a cabo; PRINCE2 proporciona un lenguaje común en los proyectos.</p>
